            $cookieVal = bin2hex($decrypted);
            echo "Calculated Cookie: __test=$cookieVal\n";
            return "__test=$cookieVal; expires=Thu, 31-Dec-37 23:55:55 GMT; path=/";
        } else {
            echo "Failed to decrypt.\n";
        }
    } else {
        echo "Could not find a, b, c variables.\nHtml:\n$html\n";
    }
    return false;
}

